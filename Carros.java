package poo;

public class Carros {

	String marca;
	String cor;
	int ano;
	int porta;
	String vireDireita;
	String vireEsquerda;
	

	public String getmarca() {
		return marca;
	}
	public void setmarca(String marca) {
		this.marca = marca;
	}
	public String getvireDireita() {
		return vireDireita;
	}
	public void setvireDireita(String vireDireita) {
		this.vireDireita = vireDireita;
	}
	public String getvireEsquerda() {
		return vireEsquerda;
	}
	public void setvireEsquerda(String vireEsquerda) {
		this.vireEsquerda = vireEsquerda;
	}
	public int getano() {
		return ano;
	}
	public void setano(int ano) {
		this.ano = ano;
	}
	public String getcor() {
		return cor;
	}
	public void setcor(String cor) {
		this.cor = cor;
	}
	public int getporta() {
		return porta;
	}
	public void setporta (int porta) {
		this.porta = porta;
	}
}

