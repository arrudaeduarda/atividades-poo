package poo;

public class Cadeira {
	
	String cor;
	String porte;
	int rodas;
	int suporte;
	String gira;
	String sobe;
	String desce;
	
	public String getcor() {
		return cor ;
	}
	public void setcor(String cor ) {
		this.cor = cor;
	}
	public String getporte() {
		return porte;
	}
	public void setporte(String porte) {
		this.porte = porte;
	}
	public int getrodas() {
		return rodas;
	}
	public void setrodas(int rodas ) {
		this.rodas = rodas;
	}
	public int getsuporte() {
		return suporte;
	}
	public void set(int suporte ) {
		this.suporte = suporte;
	}
	public String getgira() {
		return gira;
	}
	public void setgira(String gira) {
		this.gira = gira;
	}
	public String getsobe() {
		return sobe;
	}
	public void setsobe(String sobe) {
		this.sobe = sobe;
	}
	public String getdesce() {
		return desce;
	}
	public void setdesce(String desce) {
		this.desce = desce ;
	}



}
